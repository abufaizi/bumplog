global string, bump, position


class log():
    def __init__(self, position, bump):
        self.position = position
        self.bump = bump

    def set_string(str):
        self.string = str

    def get_string():
        return(string)

    def incr_bump():
        self.bump += 1

    def decr_bump():
        bump -= 1

    def reset_bump():
        bump = 0

    def set_pos(pos):
        position = pos

    def get_pos():
        return position
